<h1 class="text-3xl font-bold">IPA</h1>
<div class="pb-4">
    <p>India pale ale (IPA) is a hoppy beer style within the broader category of pale ale.</p>
    <p>IPA originated in the United Kingdom, to be exported to India, which was under the control of the British East India Company until 1858. The higher hop content of IPA acted as a natural preservative, preventing it from spoiling during the long shipping voyage.</p>
    <p>IPA declined in popularity in the late 19th and early 20th centuries. Since the 1970s, it has regained significant popularity, being associated with craft beer.</p>

